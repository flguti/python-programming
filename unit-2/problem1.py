disney_characters = ['simba', 'ariel', 'pumba', 'flounder', 'nala', 'ursula', 'scar', 'flotsam', 'timon']

for name in disney_characters:
    if "u" in name:
        print(name, 'U are so Uniquely U!')
    elif "i" in name:
        print(name, 'I bet you are impressive')
    elif "o" in name:
        print(name, 'O My')
    else:
        print(name, 'ehh')
